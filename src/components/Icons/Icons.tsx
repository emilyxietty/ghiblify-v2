/**
 * Inline SVG icons — drop-in replacements for the most-used
 * @mui/icons-material imports. Paths come straight from MUI's source
 * so the visuals are byte-identical.
 *
 * Why: each MUI icon import drags along `createSvgIcon` from
 * `@mui/material` plus the emotion runtime. Inlining the SVG path
 * avoids that scaffolding entirely. Once every MUI icon import in
 * the app has been migrated, `@mui/material`, `@mui/icons-material`,
 * `@emotion/react`, and `@emotion/styled` can be removed.
 *
 * API matches the subset of MUI's SvgIcon that the codebase actually
 * uses: `fontSize` ("small" | "medium" | "large" | "inherit" |
 * number), forwarded `style`, and any other svg-element prop.
 */

import React from "react";

type FontSizeKeyword = "small" | "medium" | "large" | "inherit";
type FontSize = FontSizeKeyword | number | string;

export interface IconProps
  extends Omit<React.SVGProps<SVGSVGElement>, "fontSize"> {
  fontSize?: FontSize;
}

// Mirrors MUI's SvgIcon size mapping. Values are CSS lengths; the
// `<svg>` itself stays at width/height: 1em so the SVG scales with
// whichever font-size resolves on it.
const KEYWORD_SIZES: Record<FontSizeKeyword, string> = {
  small: "1.25rem",
  medium: "1.5rem",
  large: "2.1875rem",
  inherit: "inherit",
};

const resolveFontSize = (size: FontSize | undefined): string | undefined => {
  if (size === undefined) return undefined;
  if (typeof size === "number") return `${size}px`;
  if (size in KEYWORD_SIZES) return KEYWORD_SIZES[size as FontSizeKeyword];
  return size;
};

const makeIcon = (path: string): React.FC<IconProps> => {
  const Icon: React.FC<IconProps> = ({ fontSize, style, ...rest }) => {
    const resolved = resolveFontSize(fontSize);
    // width/height go on the SVG as *attributes* (not inline style) so
    // any external CSS rule like `.foo svg { width: 26px }` can still
    // override the default. Inline style would beat class selectors and
    // strand consumer CSS — that bit us once already.
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 24 24"
        fill="currentColor"
        aria-hidden="true"
        width="1em"
        height="1em"
        style={
          resolved !== undefined
            ? { fontSize: resolved, ...style }
            : style
        }
        {...rest}
      >
        <path d={path} />
      </svg>
    );
  };
  return Icon;
};

